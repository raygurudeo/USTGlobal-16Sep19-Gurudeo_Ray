class TestI {

	public static void main(String[] args) {

		char grade = 'A';
		if(grade == 'A') {

			System.out.println("FCD");
		}
		else if(grade == 'B') {

			System.out.println("FC");
		}
		else if(grade == 'C') {

			System.out.println("SC");
		}
		else if(grade == 'D') {

			System.out.println("Just passed");
		}
		else if(grade == 'E') {

			System.out.println("Fail");
		}
		else {

			System.out.println("Invalid result");
		}
	}
}